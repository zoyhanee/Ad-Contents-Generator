from app.ml.clients.base import TextModelClient


def generate_post_copy(
    client: TextModelClient,
    product_name: str,
    product_description: str | None,
    platform: str,
    selected_slogan: str,
    concept: str,
    feedback: str | None = None,
) -> str:
    prompt = f"""
당신은 소상공인을 위한 전문 SNS 광고 카피라이터입니다.

아래 상품 정보와 광고 설정을 바탕으로
실제 게시물에 바로 사용할 수 있는 광고 게시글 문구를 작성하세요.

[상품 정보]
- 상품명: {product_name}
- 상품 설명: {product_description or "없음"}

[광고 정보]
- 플랫폼: {platform}
- 메인 슬로건: {selected_slogan}
- 광고 시안 콘셉트: {concept}
- 사용자 수정 요청: {feedback or "없음"}

다음 조건을 반드시 지키세요.

1. 상품의 재료, 성능, 효능, 가격, 할인, 수치 등
   사실로 검증되어야 하는 정보는 입력된 정보만 활용하세요.
2. 입력에 없는 할인, 이벤트, 판매 실적, 고객 후기,
   인증, 수상 경력은 만들지 마세요.
3. 메인 슬로건을 그대로 반복하는 데 그치지 말고,
   슬로건의 분위기를 자연스럽게 확장하세요.
4. 광고 시안 콘셉트와 어울리는 게시글 문구를 작성하세요.
5. 고객이 실제 게시물을 읽는 것처럼 자연스럽고
   매력적인 한국어로 작성하세요.
6. 사용자 수정 요청이 있다면 게시글 문구에도 자연스럽게 반영하세요.
   단, 수정 요청에 없는 상품 사실을 새롭게 만들어내지 마세요.

플랫폼별 작성 규칙:

- instagram:
  감각적이고 자연스러운 본문으로 작성하세요.
  짧은 문단과 적절한 이모지를 사용할 수 있습니다.
  마지막에 상품과 관련된 해시태그를 3~5개 포함하세요.

- baemin:
  메뉴의 맛, 향, 식감과 먹고 싶은 순간이 자연스럽게 떠오르도록 작성하세요.
  짧고 빠르게 읽히는 문장으로 구성하세요.
  고객이 주문하고 싶어지는 자연스러운 표현을 사용하세요.
  입력에 없는 할인, 쿠폰, 무료 배달, 리뷰 이벤트는 만들지 마세요.
  해시태그는 포함하지 마세요.

- naver:
  상품 특징이 명확하게 전달되는 정보성 문구로 작성하세요.
  과도한 감성 표현보다 읽기 쉬운 설명과 핵심 매력을 강조하세요.
  검색 키워드를 억지로 반복하지 마세요.

- offline:
  SNS 게시글이 아닌 홍보 안내 문구 형태로 작성하세요.
  짧고 명확하게 상품의 핵심 매력을 전달하세요.
  해시태그와 SNS식 표현은 사용하지 마세요.

출력에는 게시글 본문만 포함하세요.
제목, 설명, 마크다운 코드 블록은 출력하지 마세요.
""".strip()

    return client.generate(prompt).strip()